//& ამიტომ ეს მეთოდი სასარგებლოა მაშინ, როცა აპლიკაცია მუშაობს ბევრ ფაილთან ან ასრულებს სხვა ამოცანებს პარალელურად (მაგალითად, ვებ-სერვერში).

