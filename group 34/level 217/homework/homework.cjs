//^ ხოლო fs.writeFileSync() არის სინქრონული მეთოდი.
//& ამ შემთხვევაში, პროგრამა ჩერდება და ელოდება სანამ ფაილი ბოლომდე ჩაიწერება.
//& სანამ ჩაწერა არ დასრულდება, შემდეგი კოდი არ შესრულდება.
//& ამიტომ ეს მეთოდი მარტივ სკრიპტებშია მოსახერხებელი, მაგრამ დიდ პროექტებში შეიძლება შეაფერხოს სიჩქარე, რადგან ბლოკავს სხვა ოპერაციებს.


const fs = require('node:fs')
function readFileContent(path){
    fs.readFile(path, 'utf8', (err, data) => {
        if(err){
            console.error(err);
            return;
        }
        console.log(data);
    })
}
readFileContent('./task04.txt');


function writeToFile(path, content){
    fs.writeFile(path, content, err => {
        if(err){
            console.log(err);
            return;
        }
        console.log('File writing ended!(Task 05)');
    })
}
writeToFile('./task05.txt', 'Hello, World!');


const process = require('node:process');
let argument = process.argv.slice(2).join(' ');
fs.writeFile('./text.txt', argument, err => {
    if(err){
        console.error(err);
        return;
    }
    console.log('File writing ended(Task 06)');

    fs.appendFile('./text.txt', '\nNew line!', err => {
        if(err){
            console.error(err);
            return;
        }
        console.log('New Line added!(Task 07)');
    })

    fs.readFile('./text.txt', 'utf8', (err, data) => {
        if(err){
            console.error(err);
            return;
        }
        console.log(data);
    })
})